function getSign(month, day) {
  const m = parseInt(month, 10), d = parseInt(day, 10);
  if ((m === 3 && d >= 21) || (m === 4 && d <= 19)) return SIGNS[0];
  if ((m === 4 && d >= 20) || (m === 5 && d <= 20)) return SIGNS[1];
  if ((m === 5 && d >= 21) || (m === 6 && d <= 20)) return SIGNS[2];
  if ((m === 6 && d >= 21) || (m === 7 && d <= 22)) return SIGNS[3];
  if ((m === 7 && d >= 23) || (m === 8 && d <= 22)) return SIGNS[4];
  if ((m === 8 && d >= 23) || (m === 9 && d <= 22)) return SIGNS[5];
  if ((m === 9 && d >= 23) || (m === 10 && d <= 22)) return SIGNS[6];
  if ((m === 10 && d >= 23) || (m === 11 && d <= 21)) return SIGNS[7];
  if ((m === 11 && d >= 22) || (m === 12 && d <= 21)) return SIGNS[8];
  if ((m === 12 && d >= 22) || (m === 1 && d <= 19)) return SIGNS[9];
  if ((m === 1 && d >= 20) || (m === 2 && d <= 18)) return SIGNS[10];
  return SIGNS[11];
}

// ── Form submit ───────────────────────────────────────────────────────────────
document.getElementById('zurhaiform').addEventListener('submit', function (e) {
  e.preventDefault();

  const name  = document.getElementById('name').value.trim();
  const year  = document.getElementById('year').value;
  const month = document.getElementById('month').value;
  const day   = document.getElementById('day').value;

  if (!name || !year || !month || !day) return;

  const sign = getSign(month, day);
  renderResult(name, year, month, day, sign);
});

document.getElementById('backBtn').addEventListener('click', function () {
  document.getElementById('resultSection').classList.add('hidden');
  document.getElementById('formSection').classList.remove('hidden');
});

function renderResult(name, year, month, day, sign) {
  document.getElementById('greeting').textContent   = `Сайн байна уу, ${name}!`;
  document.getElementById('signSymbol').textContent = sign.symbol;
  document.getElementById('signName').textContent   = sign.name;
  document.getElementById('signDates').textContent  = sign.dates;

  const badge = document.getElementById('elementBadge');
  badge.textContent = sign.elementLabel;
  badge.className   = `element-badge ${sign.element}`;

  document.getElementById('personality').textContent = sign.personality;
  document.getElementById('career').textContent      = sign.career;
  document.getElementById('love').textContent        = sign.love;
  document.getElementById('health').textContent      = sign.health;
  document.getElementById('finance').textContent     = sign.finance;
  document.getElementById('advice').textContent      = sign.advice;

  const strList = document.getElementById('strengths');
  const wkList  = document.getElementById('weaknesses');
  strList.innerHTML = sign.strengths.map(s => `<li>${s}</li>`).join('');
  wkList.innerHTML  = sign.weaknesses.map(w => `<li>${w}</li>`).join('');

  const compat = document.getElementById('compatList');
  compat.innerHTML = sign.compat.map(c => `<span class="compat-chip">${c}</span>`).join('');

  document.getElementById('luckyNum').textContent    = sign.luckyNum;
  document.getElementById('luckyColor').textContent  = sign.luckyColor;
  document.getElementById('rulingPlanet').textContent = sign.planet;

  document.getElementById('formSection').classList.add('hidden');
  document.getElementById('resultSection').classList.remove('hidden');
  window.scrollTo({ top: 0, behavior: 'smooth' });
}
